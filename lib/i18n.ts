export type TKey = keyof typeof sq;

export function useTranslation() {
  const { state } = useStore();
  const lang = (state.language ?? 'sq') as AppLanguage;
  const tbl = STRINGS[lang] ?? STRINGS.sq;
  return {
    t: (key: TKey): string => tbl[key] ?? sq[key] ?? key,
    lang,
  };
}

/** Standalone translation helper for use outside React components */
export function getT(lang: AppLanguage) {
  const tbl = STRINGS[lang] ?? STRINGS.sq;
  return (key: TKey): string => tbl[key] ?? sq[key] ?? key;
}
